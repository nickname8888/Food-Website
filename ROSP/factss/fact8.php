<p>
<b>Ingredients</b>
</p>
<ul >
    <li>2 cup frozen sweet corn</li>
    <li>2 tablespoon rice flour</li>
    <li>salt as required</li>
    <li>1/2 teaspoon dry mango powder</li>
    <li>1 cup vegetable oil</li>
    <li>1/4 cup corn flour</li>
    <li>1/2 teaspoon powdered black pepper</li>
    <li>1/2 teaspoon red chilli powder</li>
    <li>1 tablespoon lemon juice</li>    
</ul>
<p>
<b>Step 1 Thaw the sweet corn</b><br />
Let the frozen sweet corn thaw and come back to room temperature.
<br />
<b>Step 2 Boil the corn</b><br />
Boil some water in a pot. Once it comes to a boil, add corn to the pot. Stir and boil for just 2 minutes. Now drain the excess water and collect the corn in a sieve.
<br />
<b>Step 3 Mix in corn flour and rice flour</b><br />
Add sweet corn to a bowl. Add rice flour and corn flour and give a good mix. Now add some salt and black pepper powder. Again mix to coat the corn well.
<br />
<b>Step 4 Dust off excess flour</b><br />
Now add the corn to a sieve and shake a bit to dust off excess flour.
<br />
<b>Step 5 Fry the corn</b><br />
Heat oil in a kadhai. Deep-fry the coated corn until crispy in texture and golden brown in colour.
<br />
<b>Step 6 Add the spices</b><br />
Add the fried corn to a bowl. Add red chilli powder, dry mango powder, salt and lemon juice. Toss well to coat the corn in the spices. Adjust salt as per taste.
<br />
