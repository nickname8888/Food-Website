<p>
<b>Ingredients</b>
</p>
<ul >
    <li>1/3 cup coconut powder</li>
    <li>1/4 cup whipping cream</li>
    <li>1/2 teaspoon vanilla extract</li>
    <li>200 gm white chocolate</li>
    <li>1 tablespoon butter</li>
    <li>4 tablespoon coconut flake</li>
</ul>
<p>
<b>Step 1 Melt the white chocolate and whipping cream</b><br />
Roughly chop the white chocolate and add it to a bowl along with whipping cream. Use the double boiler method to melt it. In this method, fill a pot halfway with water and keep it on high flame and let it come to a boil. Once the water comes to a boil, place the bowl with white chocolate on the pot. Make sure the bowl fits perfectly on the pot and not float in it. Now keep the flame low-medium and let the white chocolate melt completely.
<br />
<b>Step 2 Add butter</b><br />
Now add butter to the bowl and give a mix. Let it melt as well. Give a good mix and once a smooth mixture is formed, take it off the heat.
<br />
<b>Step 3 Add coconut powder</b><br />
Now add coconut powder to the melted chocolate along with vanilla extract. Use a spatula or spoon to mix and make a thick mixture.
<br />
<b>Step 4 Let the mixture chill</b><br />
Let the mixture cool down a bit. Now cover it with a lid or cling film and slide it in the freezer for a few hours. 2-3 hours is enough for the mixture to hold shape.
<b>Step 5 Make truffles</b><br />
Take out the mixture from the freezer. Scoop out small portions from the mixture using a spoon and roll between your hands to make balls. Roll these balls in coconut flakes to coat them from all sides.
<br />
<b>Step 6 Ready to be served</b><br />
Your Coconut Truffles are now ready to be served. Enjoy!
